# slicing with skip value
# skip value feature allows us to skip a value in print 
a = "Kartik"
print (a[1:4]) #normal series
print (a[1:4:2]) #here 2 will be placed



b = "KartikisGood"
print (b[0:]) #this will print the whole
print (b[0::2]) #this will print alternative letters
print (b[0::3]) #this will print every letter skipping two in b/w
print (b[0::5])






